# Variáveis e Tipos de Dados:
# Em python, você não precisa declarar o tipo de uma variável antes de usá-la. O tipo é inferido automaticamente pelo interpretador
# Em Python, a variável é declarada no padrão snake_case.
# 1. String ->
# 2. number - int (número inteiro) ->
# 3. number - float (número decimal) ->
# 4. boolean -> True ou False (Verdadeiro ou Falso) ->
nome = "Samara" # string
email = "samaradejesusgomes29@gmail.com" # string
cidade = "São Jão de Meriti" # string
cpf = 15490302720 # number-int
salário = 30.000 # number-float
casada = False # boolean